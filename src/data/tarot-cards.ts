export function getCardById(id: number): TarotCard | undefined {
  return tarotCards.find((card) => card.id === id);
}

export function getCardByName(name: string): TarotCard | undefined {
  return tarotCards.find(
    (card) => card.name.toLowerCase() === name.toLowerCase() || card.nameCn === name
  );
}
